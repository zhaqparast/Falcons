package JavaBasicsPackage;

public class Lesson02DataTypes {

	public static void main(String[] args) {

		
		byte b = 127; //byte takes between -128 through 127
		short s = -32768; //short takes a number that is between the range of -32768 & 32767
		int i = 98982412;
		long l = 9724390729079L; //a long data type should have the letter L at the end of the number
		float f = 9420395792f; //a float data type should have the letter F at the end of the number
		double d = 9872.23985;
		char c = 'a';
		boolean bol1 = true;
		boolean bol2 = false;
		
		
		String text1 = "this is a String";
		String text2 = new String ("this is another String");
	}

}
