package OPPSConceptsDay02AccessModifiers;

public class India {

	private String name;
	private String capitalCity;
	private String worldWonder;
	private long population;
	private int noOfStates;
	private int noOfLargeCities;

	public India() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCapitalCity() {
		return capitalCity;
	}

	public void setCapitalCity(String capitalCity) {
		this.capitalCity = capitalCity;
	}

	public String getWorldWonder() {
		return worldWonder;
	}

	public void setWorldWonder(String worldWonder) {
		this.worldWonder = worldWonder;
	}

	public long getPopulation() {
		return population;
	}

	public void setPopulation(long population) {
		this.population = population;
	}

	public int getNoOfStates() {
		return noOfStates;
	}

	public void setNoOfStates(int noOfStates) {
		this.noOfStates = noOfStates;
	}

	public int getNoOfLargeCities() {
		return noOfLargeCities;
	}

	public void setNoOfLargeCities(int noOfLargeCities) {
		this.noOfLargeCities = noOfLargeCities;
	}

}
