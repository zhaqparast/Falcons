package OPPSConceptsDay02AccessModifiers;

public class TomJerry {

	private String cartoonName;
	private String cartoonCharacters;
	private String createrName;
	private int yearFirstDisplayed;

	public TomJerry() {

	}

	public String getCartoonName() {
		return cartoonName;
	}

	public void setCartoonName(String cartoonName) {
		this.cartoonName = cartoonName;
	}

	public String getCartoonCharacters() {
		return cartoonCharacters;
	}

	public void setCartoonCharacters(String cartoonCharacters) {
		this.cartoonCharacters = cartoonCharacters;
	}

	public String getCreaterName() {
		return createrName;
	}

	public void setCreaterName(String createrName) {
		this.createrName = createrName;
	}

	public int getYearFirstDisplayed() {
		return yearFirstDisplayed;
	}

	public void setYearFirstDisplayed(int yearFirstDisplayed) {
		this.yearFirstDisplayed = yearFirstDisplayed;
	}

}
